/**
 * すべてのエージェントを取得
 * @returns {Array} すべてのエージェント
 */
export function getAllAgents() {
  return [
    ...AGENTS.duelist,
    ...AGENTS.sentinel,
    ...AGENTS.initiator,
    ...AGENTS.controller,
  ];
}

/**
 * ロールからランダムエージェントを取得
 * @param {string} role - ロール名 (duelist, sentinel, initiator, controller)
 * @returns {Object} ランダムなエージェント
 */
export function getRandomAgentByRole(role) {
  const agents = AGENTS[role];
  if (!agents || agents.length === 0) {
    return null;
  }
  return agents[Math.floor(Math.random() * agents.length)];
}

/**
 * ランダムなエージェントを取得
 * @returns {Object} ランダムなエージェント
 */
export function getRandomAgent() {
  const allAgents = getAllAgents();
  return allAgents[Math.floor(Math.random() * allAgents.length)];
}
